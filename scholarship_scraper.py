# ── MASTER FUNCTION ──────────────────────────────────────

def run_all_scrapers(db) -> int:
    print("\nStarting ScholarZone scraper pipeline...\n")
    all_scholarships = []

    groups = [
        ("Central Government (NSP)", get_central_govt),
        ("AICTE & UGC", get_aicte_ugc),
        ("DST & INSPIRE", get_dst_inspire),
        ("CBSE & NTA", get_cbse_nta),
        ("State - South India", get_state_south),
        ("State - North & West India", get_state_north_west),
        ("State - East India", get_state_east),
        ("Private & Corporate", get_private_corporate),
        ("Sector Specific", get_sector_specific),
        ("Special Categories", get_special_category),
    ]

    for name, fn in groups:
        try:
            results = fn()
            all_scholarships.extend(results)
            print(f"  {name}: {len(results)} scholarships")
        except Exception as e:
            print(f"  ERROR in {name}: {e}")

    print(f"\nTotal collected: {len(all_scholarships)} scholarships")

    if db is not None:
        print("Saving to Firestore...")
        count = to_firestore(db, all_scholarships)
        print(f"Saved {count} scholarships to Firestore!")
        return count
    else:
        return len(all_scholarships)


if __name__ == "__main__":
    total = run_all_scrapers(None)
    print(f"\nGrand Total: {total} scholarships")