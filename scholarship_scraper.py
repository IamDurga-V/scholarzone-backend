from datetime import datetime, timedelta
from typing import List, Dict


def d(days: int) -> str:
    """Returns a date string N days from now"""
    return (datetime.now() + timedelta(days=days)).strftime("%Y-%m-%d")


def to_firestore(db, scholarships: List[Dict]) -> int:
    count = 0
    col = db.collection("scholarships")
    for s in scholarships:
        try:
            doc_id = s.get("source_id", "").replace(" ", "_").lower()
            if not doc_id:
                continue
            col.document(doc_id).set({
                **s,
                "is_active": True,
                "last_scraped": datetime.utcnow().isoformat()
            })
            count += 1
        except Exception as e:
            print(f"Error saving {s.get('name')}: {e}")
    return count


